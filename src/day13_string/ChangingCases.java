package day13_string;

public class ChangingCases {
public static void main(String[] args) {
	
	
	String str = "Hello";
	System.out.println(str);
	
	str = str.toLowerCase();
	System.out.println(str);
	
	str = str.toUpperCase();
	System.out.println(str);
	
	String str2 = "PIM"; 
	str2 =str2.toLowerCase();
	System.out.println(str2);
}
}
